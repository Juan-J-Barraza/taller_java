package staticModifier;

public class Math {
    


    public static int sum(int a, int b) {
        return a + b;
    }

    public static int rest(int a, int b) {
        return a - b;
    }


    public static int mult(int a, int b) {
        return a * b;
    }


    public static int divi(int a, int b) {
        return a / b;
    }
    

}
